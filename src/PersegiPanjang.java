

public class PersegiPanjang {
    public double luasPP(double p, double l){
        double luaspp;
        luaspp = p * l;
        return luaspp;
    }

    public double kelilingPP(double p, double l){
        double kelilingpp;
        kelilingpp = 2 * (p + l);
        return kelilingpp;
    }
}
