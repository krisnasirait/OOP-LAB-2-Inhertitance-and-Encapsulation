public class Lingkaran {
    public double luasLingkaran(double r){
        double luasling;
        luasling = 3.14 * r * r;
        return luasling;
    }

    public double kelilingLingkaran(double r){
        double kelilingling;
        kelilingling = 2 * 3.14 * r;
        return kelilingling;
    }
}
